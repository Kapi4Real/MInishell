#include <minishell.h>

extern int	g_sig;

void	normal_c(int signal)
{
	g_sig = signal;
	write(1, "\n", 2);
	rl_on_new_line();
	rl_replace_line("", 0);
	rl_redisplay();
}

void	heredoc_c(int signal)
{
	char	c;

	g_sig = signal;
	c = '\n';
	ioctl(0, TIOCSTI, &c);
	rl_on_new_line();
	rl_replace_line("", 0);
}
